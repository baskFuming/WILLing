package com.xxx.willing.model.http.js;

public class ShopJsVo {

    private int id;

    private int num;

    private int color;

    private int sizeId;

    public void setColor(int color) {
        this.color = color;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public void setSizeId(int sizeId) {
        this.sizeId = sizeId;
    }

    public int getId() {
        return id;
    }

    public int getColor() {
        return color;
    }

    public int getNum() {
        return num;
    }

    public int getSizeId() {
        return sizeId;
    }
}


